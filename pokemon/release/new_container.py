def gen_storage_container(name, mongoMap):
	"""
	name: ex: crossdb.cn.1
	"""
	domain = name.split('.')
	serv, lang, id = domain

	services = []
	kwargs = {"name": name}

	if serv == "crossdb":
		kwargs["pool_limit"] = 10
		dbname = "cross_%s_%s" % (lang, id)
		if lang == "dev":
			dbname = "cross_%s%s" % (lang, id)
	elif serv == "storage":
		dbname = "game_%s_%s" % (lang, id)
		if lang == "dev":
			dbname = "game_%s%s" % (lang, id)
		elif lang == "shenhe":
			dbname = "game_%s%s" % (lang, id)
	elif serv == "storagemerge":
		dbname = "gamemerge_%s_%s" % (lang, id)
		if lang == "dev":
			dbname = "gamemerge_%s%s" % (lang, id)
		elif lang == "shenhe":
			dbname = "gamemerge_%s%s" % (lang, id)
	elif serv == "accountdb":
		dbname = "account"
	elif serv == "giftdb":
		dbname = "gift"
	elif serv == "paymentdb":
		dbname = "order"
	else:
		raise

	if lang == "dev":
		mongodb = "192.168.1.96:27018"
	elif lang == "shenhe":
		mongodb = "127.0.0.1:27018"
	else:
		mongodb = "%s/%s?authMechanism=SCRAM-SHA-1&authSource=admin" % (mongoMap[name], dbname)

	kwargs["mongodb"] = mongodb
	kwargs["dbname"] = dbname

	if name == "account.dev.1":
		kwargs["alias"] = ["giftdb.dev.1", "paymentdb.dev.1"]

	services.append(kwargs)

	conf = {"services": services}

	if lang in NSQD_MAP:
		conf.update(NSQD_MAP[lang])
	else:
		conf.update(NSQD_MAP['cn'])

	if lang == "dev" or lang == "shenhe":
		conf["debug"] = True

	if domain[0] == "accountdb":
		domain[0] = "account"
		name = '.'.join(domain)
	elif domain[0] == "giftdb":
		domain[0] = "gift"
		name = '.'.join(domain)
	return {name: conf, }


def get_alias_servers(key):
	servers = []
	for server in MergeServs[key]:
		if 'merge' in server:
			servers += get_alias_servers(server)
		else:
			servers.append(server)
	return servers


def get_merged_ids(key):
	servers = get_alias_servers(key.replace('pvp', 'game'))
	return [s.split('.')[-1] for s in servers]


def gen_pvp_container(name, isMerged=False):
	_, lang, _ = name.split('.')

	merged_ids = None
	if isMerged:
		merged_ids = get_merged_ids(name)

	services = []
	for serv in ["pvp", "clone", "arena", "union", "union_fight", "craft", "yyhuodong", "card_fight", "gym", "hunting"]:
		kwargs = {"name": name.replace('pvp', serv)}

		if isMerged and merged_ids:
			kwargs['alias'] = ['%s.%s.%s' % (serv, lang, num) for num in merged_ids]

		if serv in ["pvp", "clone", "arena", "union", "yyhuodong", "card_fight", "hunting"]:
			kwargs['dependent'] = [
				name.replace('pvp', 'storage'),
			]
		elif serv in ["union_fight", "craft"]:
			kwargs['dependent'] = [
				name.replace('pvp', 'storage'),
				name.replace('pvp', 'game'),
				"anticheat",
			]
		elif serv in ['gym']:
			kwargs['dependent'] = [
				name.replace('pvp', 'storage'),
				name.replace('pvp', 'game'),
			]
		else:
			raise

		services.append(kwargs)

	conf = {"services": services}

	if lang in NSQD_MAP:
		conf.update(NSQD_MAP[lang])
	else:
		conf.update(NSQD_MAP['cn'])

	if lang == "dev" or lang == "shenhe":
		conf["debug"] = True
	if lang not in ('xy', 'dev', 'shenhe'):
		conf["shushu"] = True

	return {name: conf, }


