def get_lang_defines(ty, langs, isMerge):
	if ty == 'storage':
		if isMerge:
			from_file = './storage/defines_merge.json'
			to_file = './storage/defines_%s_merge.json'
			filter_str = 'merge.%s.'
		else:
			from_file = './storage/defines.json'
			to_file = './storage/defines_%s.json'
			filter_str = '.%s.'
	elif ty == 'pvp':
		if isMerge:
			from_file = './pvp/defines_merge.json'
			to_file = './pvp/defines_%s_merge.json'
			filter_str = 'merge.%s.'
		else:
			from_file = './pvp/defines.json'
			to_file = './pvp/defines_%s.json'
			filter_str = '.%s.'

	# 读取已有配置
	with open(from_file, 'r') as f:
		defines = json.loads(f.read())

	for lang in langs:
		lang_keys = [key for key in defines.keys() if filter_str % lang in key]
		lang_defines = {}
		for key in lang_keys:
			lang_defines[key] = defines.pop(key)

		if len(lang_defines) > 0:
			with open(to_file % lang, 'w') as f:
				json.dump(lang_defines, f, sort_keys=True, indent=2)

	with open(from_file, 'w') as f:
		json.dump(defines, f, sort_keys=True, indent=2, separators=(',', ': '))


